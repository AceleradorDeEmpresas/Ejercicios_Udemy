# Formato a cadenas de Python 
var_hola = "Hola"
var_mundo = "Mundo"

# Imprimir multiples variables
print(var_hola, var_mundo)

# Concatenación de cadenas
var_hola_mundo = var_hola + ' '  + var_mundo
print(var_hola_mundo)

# Interpolación de cadenas
print(f"{var_hola} {var_mundo}")

# Interpolación con multilineas
print(f"""{var_hola}
============
{var_mundo}""")




