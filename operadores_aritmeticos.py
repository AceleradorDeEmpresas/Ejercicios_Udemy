# Operadores Aritméticos 
print("*** Operadores aritméticos ***")
num_1 = 5
num_2 = 4

# Operador de suma +
print("Total: ", num_1 + num_2)
# Operador de resta -
print("Total:", num_1 - num_2)
# Operador de multiplicación *
print("Total: ", num_1 * num_2)
# Operador de división /  
print("Total: ", num_1 / num_2)
# Operador de módulo %
print("Total: ", 2 % 3)
# Operador de potencia **
print("Total: ", num_1 ** num_2)
